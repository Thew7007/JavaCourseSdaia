package day1;

public class NestedLoop {
    public static void main(String[] args) {

        for (int y =1; y <10; y++){
            for (int x =1; x<= 10; x++){
                System.out.println(x + "\t");
            }
            System.out.println();
            System.out.println("=========");
        }
        System.out.println("=========");
    }

}
